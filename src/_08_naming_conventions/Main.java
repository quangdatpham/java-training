package _08_naming_conventions;

public class Main {
    // Naming conventions
    // Packages
    // - lowercase
    // - unique
    // - internet domain name
    // Ex:
    // - com.example._1_helloworld
    // - com.example.hello_world
    // - java.lang
    // - java.io
    // - org.xml.sax.helpers

    // Classes
    // - PascalCase
    // - should be nouns (represent things)
    // Ex:
    // - HashMap
    // - LinkedList


    // Interfaces
    // - PascalCase
    // Ex:
    // - List
    // - Serializable

    // Methods
    // - camelCase
    // - should be verbs
    // Ex:
    // - size()
    // - getName()

    // Constants
    // - UPPER_CASE
    // - separator `_`
    // - use with `final` keyword
    // Ex:
    // - Math.PI
    // - Math.DEGREES_TO_RADIANS

    // Variables
    // camelCase

    // Type Parameters
    // - Single capitalized character
    // E - Element
    // K - Key
    // N - Number
    // T - Type
    // V - Value
    // S,U,V etc. - 2nd, 3rd, 4th types
}
